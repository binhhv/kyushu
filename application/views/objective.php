        <h2 class="titletype-1">目的</h2>
<div class="box">
  <dl>
    <dt>１．放射線安全管理</dt>
    <dd>全学の放射線障害の防止に関する組織である九州大学放射線障害防止委員会と連携し、<br />
        全学のＲＩ施設における放射線安全管理の中心となる。</dd>
    <dt>２．教育訓練</dt>
    <dd>放射性同位元素等を利用するにあたって必要な教育訓練を実施する。<br />
        各学部におけるＲＩ取扱実習のための施設・設備を提供する。</dd>
    <dt>３．共同利用</dt>
    <dd>全学の放射性同位元素を利用する研究、教育のために、研究施設、設備機器等を提供し、<br />
        研究、教育の推進を図るとともに、さらに高度な新しい研究を受け入れるための施設、設備の充実・整備を図る。</dd>
    <dt>４．情報収集</dt>
    <dd>ＲＩの利用やＲＩ管理に関する最新の情報を収集・提供し、研究・管理の支援・進展を図る。</dd>
    <dt>５．地域への貢献</dt>
    <dd>他大学、民間のアイソトープ施設の管理・運営の模範となり、<br />
        また、地域社会における放射線安全知識の普及等に協力し、原子力平和利用の推進を図る。</dd>
  </dl>
